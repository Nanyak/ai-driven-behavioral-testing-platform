import type { MutationMetrics, MutationOperator, MutationResult } from "./types.js";

const OPERATORS: MutationOperator[] = [
  "drop_field",
  "null_field",
  "retype_field",
  "empty_array",
  "enum_violation",
  "const_violation",
  "range_violation",
  "format_violation",
  "status_change",
];

export interface BuildMetricsInput {
  target: string;
  baselineClean: boolean;
  executabilityRate: number;
  results: MutationResult[];
}

export function pct(n: number): string {
  return `${Math.round(n * 100)}%`;
}

export function buildMetrics(input: BuildMetricsInput): MutationMetrics {
  const killed = input.results.filter((r) => r.verdict === "killed").length;
  const survived = input.results.filter((r) => r.verdict === "survived").length;
  const inconclusive = input.results.filter((r) => r.verdict === "inconclusive").length;
  const denominator = killed + survived;
  const byOperator = Object.fromEntries(
    OPERATORS.map((operator) => [
      operator,
      {
        killed: input.results.filter((r) => r.mutant.operator === operator && r.verdict === "killed").length,
        survived: input.results.filter((r) => r.mutant.operator === operator && r.verdict === "survived").length,
      },
    ])
  ) as MutationMetrics["by_operator"];

  return {
    generated_at: new Date().toISOString(),
    target: input.target,
    total_mutants: input.results.length,
    killed,
    survived,
    inconclusive,
    mutation_score: denominator > 0 ? killed / denominator : 0,
    executability_rate: input.executabilityRate,
    baseline_clean: input.baselineClean,
    by_operator: byOperator,
    survivors: input.results
      .filter((r) => r.verdict === "survived")
      .map((r) => ({
        endpoint: r.mutant.endpoint,
        status: r.mutant.status,
        operator: r.mutant.operator,
        path: r.mutant.path,
        id: r.mutant.id,
      })),
    results: input.results,
  };
}

export function renderConsole(metrics: MutationMetrics): string {
  const lines = [
    "",
    `Mutation Evaluation (target: ${metrics.target})`,
    `  Baseline: ${metrics.baseline_clean ? "clean" : "not green"}  Executability: ${pct(metrics.executability_rate)}`,
    `  Mutants: total ${metrics.total_mutants}  killed ${metrics.killed}  survived ${metrics.survived}  inconclusive ${metrics.inconclusive}`,
    `  Mutation score: ${pct(metrics.mutation_score)}`,
  ];
  if (metrics.survivors.length > 0) {
    lines.push("  Survivors:");
    for (const survivor of metrics.survivors.slice(0, 20)) {
      lines.push(`    ${survivor.operator} ${survivor.endpoint} ${survivor.path ?? "<status>"}`);
    }
    if (metrics.survivors.length > 20) {
      lines.push(`    ... ${metrics.survivors.length - 20} more`);
    }
  }
  return lines.join("\n");
}

function esc(value: string): string {
  return value.replace(/[&<>"]/g, (char) => {
    if (char === "&") return "&amp;";
    if (char === "<") return "&lt;";
    if (char === ">") return "&gt;";
    return "&quot;";
  });
}

