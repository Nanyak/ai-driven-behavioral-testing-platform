/**
 * Evaluation metrics: the shape the harness emits and its console/JSON/HTML
 * renderings. Deliberately covers only the axes derivable WITHOUT human ground
 * truth — regression detection (seeded faults), script executability, and false
 * positives (report Bảng 18). Journey-detection accuracy and review usability
 * need QA labeling and are reported as "manual", never fabricated.
 */
import type { EvalFault, FaultClass } from "./catalog.js";

export interface FaultResult {
  id: string;
  title: string;
  faultClass: FaultClass;
  targetEndpoint: string;
  expectedSignal: string;
  /** Detected as a net-new failure on the target endpoint. */
  caught: boolean;
  catchingTest: string | null;
  evidence: string | null;
  /** Set when the fault run could not be measured (restart/health failure). */
  unmeasurable: string | null;
}

export interface BaselineMetrics {
  executed: number;
  passed: number;
  failed: number;
  skipped: number;
  /** passed / executed, in [0,1]; 0 when nothing executed. */
  executability_rate: number;
  /** Titles of tests failing on a CLEAN SUT — should be empty. */
  false_positives: string[];
  clean: boolean;
}

export interface EvalMetrics {
  generated_at: string;
  target: string;
  baseline: BaselineMetrics | null;
  faults: FaultResult[];
  /** caught / measurable-faults, in [0,1]. */
  regression_detection_rate: number;
  measurable_faults: number;
}

export function pct(n: number): string {
  return `${(n * 100).toFixed(0)}%`;
}

export function buildBaselineMetrics(totals: {
  executed: number;
  passed: number;
  failed: number;
  skipped: number;
}, failedTitles: string[]): BaselineMetrics {
  return {
    ...totals,
    executability_rate: totals.executed > 0 ? totals.passed / totals.executed : 0,
    false_positives: failedTitles,
    clean: totals.failed === 0,
  };
}

export function summarizeDetection(faults: FaultResult[]): {
  regression_detection_rate: number;
  measurable_faults: number;
} {
  const measurable = faults.filter((f) => f.unmeasurable === null);
  const caught = measurable.filter((f) => f.caught).length;
  return {
    regression_detection_rate: measurable.length > 0 ? caught / measurable.length : 0,
    measurable_faults: measurable.length,
  };
}

/* ---- Renderers ---- */

export function renderConsole(m: EvalMetrics): string {
  const lines: string[] = [];
  lines.push(`\nRegression Evaluation  (target: ${m.target})`);
  if (m.baseline) {
    const b = m.baseline;
    lines.push(
      `  Baseline (clean SUT):  executed ${b.executed}  passed ${b.passed}  failed ${b.failed}  skipped ${b.skipped}`
    );
    lines.push(`    Executability rate:  ${pct(b.executability_rate)}${b.clean ? "" : "  ⚠ baseline not green"}`);
    if (b.false_positives.length > 0) {
      lines.push(`    False positives:     ${b.false_positives.join(", ")}`);
    }
  } else {
    lines.push(`  Baseline: skipped (--no-baseline)`);
  }
  lines.push(`  Seeded faults:`);
  for (const f of m.faults) {
    const mark = f.unmeasurable ? "∅" : f.caught ? "✓" : "✗";
    const note = f.unmeasurable
      ? ` UNMEASURABLE — ${f.unmeasurable}`
      : f.caught
        ? ` caught by "${f.catchingTest}"`
        : ` MISSED`;
    lines.push(`    ${mark} [${f.faultClass}] ${f.id}${note}`);
    if (f.caught && f.evidence) lines.push(`        ${f.evidence}`);
  }
  lines.push(
    `  Regression detection rate: ${pct(m.regression_detection_rate)}  (${
      m.faults.filter((f) => f.caught).length
    }/${m.measurable_faults} measurable faults caught)`
  );
  lines.push(`  Manual axes (need QA ground truth): journey-detection accuracy, review usability.`);
  return lines.join("\n");
}

function esc(s: string): string {
  return s.replace(/[&<>]/g, (c) => (c === "&" ? "&amp;" : c === "<" ? "&lt;" : "&gt;"));
}

export function renderHtml(m: EvalMetrics): string {
  const rows = m.faults
    .map((f) => {
      const status = f.unmeasurable
        ? `<span class="warn">unmeasurable</span>`
        : f.caught
          ? `<span class="ok">caught</span>`
          : `<span class="bad">missed</span>`;
      const detail = f.unmeasurable ?? (f.caught ? `${esc(f.catchingTest ?? "")} — ${esc(f.evidence ?? "")}` : esc(f.expectedSignal));
      return `<tr><td>${status}</td><td>${esc(f.faultClass)}</td><td class="mono">${esc(f.id)}</td><td class="mono">${esc(f.targetEndpoint)}</td><td>${detail}</td></tr>`;
    })
    .join("\n");
  const b = m.baseline;
  const baselineBlock = b
    ? `<p>Baseline (clean SUT): executed ${b.executed}, passed ${b.passed}, failed ${b.failed}, skipped ${b.skipped} — executability <strong>${pct(b.executability_rate)}</strong>${b.clean ? "" : " <span class='warn'>(not green)</span>"}${b.false_positives.length ? `<br/>False positives: ${b.false_positives.map(esc).join(", ")}` : ""}</p>`
    : `<p>Baseline skipped.</p>`;
  return `<!-- generated by services/test-runner/src/eval -->
<style>
  :root { color-scheme: light dark; font-family: ui-sans-serif, system-ui, sans-serif; }
  body { margin: 2rem; max-width: 60rem; }
  table { border-collapse: collapse; width: 100%; margin-top: 1rem; }
  th, td { border: 1px solid #8884; padding: .4rem .6rem; text-align: left; vertical-align: top; }
  .mono { font-family: ui-monospace, monospace; font-size: .85em; }
  .ok { color: #1a7f37; font-weight: 600; } .bad { color: #cf222e; font-weight: 600; } .warn { color: #9a6700; font-weight: 600; }
  .rate { font-size: 1.6rem; font-weight: 700; }
</style>
<h1>Regression Evaluation</h1>
<p>Generated ${esc(m.generated_at)} · target <span class="mono">${esc(m.target)}</span></p>
<p class="rate">Detection rate: ${pct(m.regression_detection_rate)} <span style="font-size:1rem;font-weight:400">(${m.faults.filter((f) => f.caught).length}/${m.measurable_faults} measurable faults caught)</span></p>
${baselineBlock}
<table><thead><tr><th>Result</th><th>Class</th><th>Fault id</th><th>Target endpoint</th><th>Detail</th></tr></thead>
<tbody>
${rows}
</tbody></table>
<p><em>Manual axes (need QA ground truth): journey-detection accuracy, review usability.</em></p>`;
}
